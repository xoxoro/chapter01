package com.javaex.ex01;

public class Ex08 {

	public static void main(String []args) {
		
		//관계연산자
		int n1 = 5;
		int n2 = 3;
		
		//n1<n2 //5<3 --> false
		//n1>n2 //5>3 --> true
		
		System.out.println(n1<n2);
		System.out.println(n1>n2);
		System.out.println(n1<=n2); 
		System.out.println(n1>=n2);
		System.out.println(n1==n2);//5==3 --> false
		System.out.println(n1!=n2);//5!=3-->true
		
		
		
	}
	
	
}
